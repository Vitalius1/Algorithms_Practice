// ================ Return if a String is a palindrome =====================
// -------------------------------------------------------------------------
// -------------------------------------------------------------------------

// ++++++ Using split(), reverse(), join() +++++
function isPalindrome_1(string) {
    let strReverse = string.split('').reverse().join('');
    return string === strReverse;
}

